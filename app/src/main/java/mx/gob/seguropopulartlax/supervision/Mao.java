package mx.gob.seguropopulartlax.supervision;

public class Mao {

    String mao;
    String alias;

    public Mao() {
    }

    public Mao(String mao, String alias) {
        this.mao = mao;
        this.alias = alias;
    }

    public String getMao() {
        return mao;
    }

    public void setMao(String mao) {
        this.mao = mao;
    }

    public String getAlias() {
        return alias;
    }

    public void setAlias(String alias) {
        this.alias = alias;
    }
}
